public class ifloop {
    public static void main(String[] args){
        int i=10;
        if(i<15) {
            System.out.println("less than 15");
        }
        else if(i>15){
            System.out.println("greater than 15");
        }
        else{
            System.out.println("none of the above");
        }
    }
}
